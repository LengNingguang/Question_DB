// ============================================================
// 文件：src/main.rs
// 说明：程序的入口点，异步 Rust 应用程序的启动流程
// ============================================================

// 导入外部库的类型和函数
// anyhow::Result 是一个通用的错误处理类型，简化错误管理
use anyhow::Result;

// 从 qb_api 库中导入需要的模块
// api::{router, AppState} - HTTP 路由构建函数和应用状态结构体
// config::AppConfig - 应用配置管理
// db::create_pool - 创建数据库连接池
use qb_api::{
    api::{router, AppState},
    config::AppConfig,
    db::create_pool,
};

// 导入 Tokio 的 TCP 监听器，用于创建 HTTP 服务器
use tokio::net::TcpListener;

// #[tokio::main] 是一个宏，它将 async main 函数转换为 Tokio 异步运行时
// 这意味着程序可以使用 async/await 语法
#[tokio::main]
async fn main() -> Result<()> {
    // ========================================================
    // 步骤 1: 初始化日志系统
    // ========================================================
    // tracing_subscriber 是 Rust 的结构化日志库
    // 它允许程序在运行时输出调试信息、错误信息等
    tracing_subscriber::fmt()
        // 配置日志级别过滤器
        // 尝试从环境变量 RUST_LOG 读取配置
        // 如果没有设置，默认使用 "info" 级别
        .with_env_filter(
            tracing_subscriber::EnvFilter::try_from_default_env()
                .unwrap_or_else(|_| "info".into()),
        )
        // 完成日志系统初始化
        .init();

    // ========================================================
    // 步骤 2: 加载应用配置
    // ========================================================
    // AppConfig::from_env() 从环境变量读取配置
    // 需要的环境变量:
    //   - QB_DATABASE_URL: PostgreSQL 数据库连接字符串
    //   - QB_BIND_ADDR: HTTP 服务器监听地址 (可选，默认 127.0.0.1:8080)
    let cfg = AppConfig::from_env()?;

    // ========================================================
    // 步骤 3: 创建数据库连接池
    // ========================================================
    // create_pool 创建 PostgreSQL 连接池
    // 连接池管理多个数据库连接，提高性能
    let pool = create_pool(&cfg.database_url).await?;

    // ========================================================
    // 步骤 4: 构建 HTTP 应用
    // ========================================================
    // router 函数创建完整的路由配置
    // AppState 包含共享资源 (这里是数据库连接池)
    let app = router(AppState { pool });

    // ========================================================
    // 步骤 5: 绑定监听端口
    // ========================================================
    // TcpListener::bind 在指定地址创建 TCP 监听器
    let listener = TcpListener::bind(cfg.bind_addr).await?;

    // ========================================================
    // 步骤 6: 记录启动信息并启动服务
    // ========================================================
    // tracing::info! 记录 INFO 级别的日志
    // addr = %cfg.bind_addr 使用 Display 格式化输出地址
    tracing::info!(addr = %cfg.bind_addr, "qb_api_rust listening");

    // ========================================================
    // 步骤 7: 启动 HTTP 服务器
    // ========================================================
    // axum::serve 启动 HTTP 服务器
    // 这是一个异步调用，会一直运行直到服务器关闭
    axum::serve(listener, app).await?;

    // 返回 Ok(()) 表示程序成功退出
    Ok(())
}

/*
 * ============================================================
 * 知识点讲解 (Rust 新手必读)
 * ============================================================
 *
 * 1. async/await 是什么？
 *    - async 函数返回一个 Future，不会立即执行
 *    - await 等待 Future 完成，期间不阻塞其他任务
 *    - 适合 I/O 密集型操作 (网络请求、数据库查询等)
 *
 * 2. ? 操作符的作用
 *    - 如果结果是 Ok，提取其中的值
 *    - 如果结果是 Err，立即返回错误
 *    - 简化错误处理，避免嵌套 match
 *
 * 3. Result<T, E> 类型
 *    - Rust 的标准错误处理类型
 *    - Ok(T) 表示成功，包含返回值
 *    - Err(E) 表示失败，包含错误信息
 *
 * 4. anyhow::Result 是什么？
 *    - 简化的 Result 类型
 *    - 等同于 Result<T, anyhow::Error>
 *    - 适合应用程序层的错误处理
 *
 * ============================================================
 * 程序启动流程图
 * ============================================================
 *
 * 程序启动
 *     ↓
 * 初始化日志系统
 *     ↓
 * 从环境变量读取配置
 *     ↓
 * 创建数据库连接池 (异步)
 *     ↓
 * 构建 HTTP 路由和中间件
 *     ↓
 * 绑定 TCP 端口
 *     ↓
 * 启动服务器 (无限循环，等待请求)
 *
 */
