//! 数据库连接模块
//!
//! 负责创建和管理 PostgreSQL 数据库连接池
//! 连接池可以避免频繁创建/销毁连接的开销

// 导入 anyhow 错误处理库
use anyhow::{Context, Result};

// 导入 SQLx 数据库库的 PostgreSQL 相关类型
// PgPoolOptions: 配置连接池选项
// PgPool: 连接池类型，管理多个数据库连接
use sqlx::{postgres::PgPoolOptions, PgPool};

// ============================================================
// 创建数据库连接池
// ============================================================
// async 表示这是一个异步函数，返回 Future
// &str 是字符串切片类型，不获取所有权
pub async fn create_pool(database_url: &str) -> Result<PgPool> {
    // ========================================================
    // 步骤 1: 创建连接池配置
    // ========================================================
    // PgPoolOptions::new() 创建默认配置
    PgPoolOptions::new()
        // ====================================================
        // 步骤 2: 配置最大连接数
        // ====================================================
        // 设置连接池最多保持 10 个活跃连接
        // 超过此数量时，新的请求需要等待
        .max_connections(10)

        // ====================================================
        // 步骤 3: 建立数据库连接
        // ====================================================
        // .connect() 尝试连接到数据库
        // .await 等待异步操作完成
        // 返回 Result<PgPool, sqlx::Error>
        .connect(database_url)
        .await

        // ====================================================
        // 步骤 4: 添加错误上下文
        // ====================================================
        // .context() 在错误时添加描述信息
        // 帮助调试时快速定位问题
        .context("failed to connect postgres")
}

/*
 * ============================================================
 * 知识点讲解 (Rust 新手必读)
 * ============================================================
 *
 * 1. 连接池是什么？
 *    - 预先创建多个数据库连接
 *    - 请求时复用已有连接
 *    - 避免频繁创建/销毁连接的开销
 *    - 控制并发连接数，防止数据库过载
 *
 * 2. &str vs String
 *    - &str: 字符串切片，借用视角，不拥有数据
 *    - String: 拥有所有权的字符串
 *    - 函数参数常用 &str，更灵活
 *
 * 3. async/await 在数据库操作中的作用
 *    - 数据库 I/O 是耗时操作
 *    - async 允许其他任务在等待时运行
 *    - 提高服务器并发处理能力
 *
 * 4. 方法链 (Method Chaining)
 *    - 每个方法返回 self 或相关类型
 *    - 可以连续调用多个方法
 *    - 常见于构建器模式 (Builder Pattern)
 *
 * ============================================================
 * 连接池工作流程图
 * ============================================================
 *
 * 程序启动
 *     ↓
 * create_pool()
 *     ↓
 * 创建 PgPoolOptions
 *     ↓
 * 设置 max_connections = 10
 *     ↓
 * 连接到 PostgreSQL
 *     ↓
 * 返回 PgPool (连接池句柄)
 *
 * 后续请求处理:
 * 需要数据库操作 → 从池中获取连接 → 执行查询 → 归还连接
 *
 */
