// pub 表示其他文件可以访问这个模块
pub mod api;

// 声明 config 模块为公共模块
// 包含应用配置管理相关代码
pub mod config;

// 声明 db 模块为公共模块
// 包含数据库连接管理相关代码
pub mod db;

/*
 * ============================================================
 * 知识点讲解：Rust 模块系统
 * ============================================================
 *
 * 1. mod 关键字
 *    - 声明一个模块
 *    - 模块可以 nested (嵌套)
 *    - 默认是私有的，外部无法访问
 *
 * 2. pub 关键字
 *    - pub mod: 公开模块本身
 *    - pub(crate) mod: 只在 crate 内部公开
 *    - pub(super) mod: 对父模块公开
 *
 * 3. 模块文件对应规则
 *    - mod api; 对应 api.rs 或 api/mod.rs
 *    - mod api; 在 lib.rs 中声明后，编译器会查找这两个文件
 *
 * 4. ! 文档注释
 *    - //! 表示对当前 item 的文档说明
 *    - /// 表示对下一个 item 的文档说明
 *    - 支持 Markdown 格式
 *
 * ============================================================
 * 本项目模块结构图
 * ============================================================
 *
 * qb_api (lib.rs)
 *     ├── api          HTTP API 层
 *     │   ├── shared   共享工具和错误处理
 *     │   ├── system   系统接口 (健康检查)
 *     │   ├── questions题目管理接口
 *     │   ├── papers   试卷管理接口
 *     │   └── ops      运维操作接口
 *     ├── config       配置管理
 *     └── db           数据库连接管理
 *
 */
